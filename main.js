function initDomCursorTech(layer) {
  const elements = [];
  let frame = 0;
  let lastSpawn = 0;
  let lastPoint = null;

  function clearEffect() {
    if (frame) window.cancelAnimationFrame(frame);
    frame = 0;
    elements.splice(0).forEach((item) => item.el.remove());
    lastPoint = null;
  }

  function scheduleRender() {
    if (window.__mouseEffectsPaused) return;
    if (!frame) {
      frame = window.requestAnimationFrame(render);
    }
  }

  function trimElements() {
    while (elements.length > 34) {
      const item = elements.shift();
      item.el.remove();
    }
  }

  function addNode(x, y) {
    const node = document.createElement("span");
    node.className = "cursor-node";
    layer.appendChild(node);
    elements.push({
      el: node,
      type: "node",
      x,
      y,
      vx: (Math.random() - 0.5) * 0.32,
      vy: (Math.random() - 0.5) * 0.32,
      life: 1
    });
  }

  function addBit(x, y) {
    const bit = document.createElement("span");
    bit.className = "cursor-bit";
    bit.textContent = Math.random() > 0.5 ? "1" : "0";
    layer.appendChild(bit);
    elements.push({
      el: bit,
      type: "bit",
      x: x + 8 + Math.random() * 18,
      y: y - 12 + Math.random() * 22,
      vx: (Math.random() - 0.5) * 0.18,
      vy: -0.09 - Math.random() * 0.12,
      life: 1
    });
  }

  function addLink(from, to) {
    const length = Math.hypot(to.x - from.x, to.y - from.y);
    if (length < 8 || length > 210) return;

    const link = document.createElement("span");
    link.className = "cursor-link";
    link.style.width = `${length}px`;
    layer.appendChild(link);
    elements.push({
      el: link,
      type: "link",
      x: from.x,
      y: from.y,
      angle: Math.atan2(to.y - from.y, to.x - from.x),
      life: 1
    });
  }

  function render() {
    frame = 0;
    if (window.__mouseEffectsPaused) {
      clearEffect();
      return;
    }

    for (let index = elements.length - 1; index >= 0; index -= 1) {
      const item = elements[index];
      item.life -= item.type === "link" ? 0.022 : 0.016;

      if (item.life <= 0) {
        item.el.remove();
        elements.splice(index, 1);
        continue;
      }

      if (item.type === "link") {
        item.el.style.opacity = String(item.life * 0.82);
        item.el.style.transform = `translate3d(${item.x}px, ${item.y}px, 0) rotate(${item.angle}rad)`;
      } else {
        item.x += item.vx;
        item.y += item.vy;
        item.el.style.opacity = String(item.life);
        item.el.style.transform = `translate3d(${item.x}px, ${item.y}px, 0)`;
      }
    }

    if (elements.length) {
      frame = window.requestAnimationFrame(render);
    }
  }

  window.addEventListener("pointermove", (event) => {
    if (window.__mouseEffectsPaused) return;
    const now = performance.now();
    if (now - lastSpawn < 78) return;

    const point = { x: event.clientX, y: event.clientY };
    addNode(point.x, point.y);
    addBit(point.x, point.y);
    if (lastPoint) addLink(lastPoint, point);
    lastPoint = point;
    lastSpawn = now;
    trimElements();
    scheduleRender();
  }, { passive: true });

  window.addEventListener("ticket-run-effects", (event) => {
    if (event.detail?.paused) clearEffect();
  });

  window.__cursorTechEnabled = "dom";
}

