    function applyLanguage(language, persist = false) {
      const nextLanguage = supportedLanguages.includes(language) ? language : "en";
      const dictionary = translations[nextLanguage];

      document.querySelectorAll("[data-i18n]").forEach((node) => {
        const key = node.dataset.i18n;
        if (dictionary[key]) node.textContent = dictionary[key];
      });

      document.querySelectorAll("[data-i18n-html]").forEach((node) => {
        const key = node.dataset.i18nHtml;
        if (!dictionary[key]) return;
        if (richTextKeys.has(key)) {
          node.innerHTML = dictionary[key];
          return;
        }
        node.textContent = dictionary[key];
      });

      html.lang = nextLanguage === "cs" ? "cs" : nextLanguage;
      document.title = "MN | Digital";

      buttons.forEach((button) => {
        const active = button.dataset.lang === nextLanguage;
        button.classList.toggle("active", active);
        button.setAttribute("aria-pressed", String(active));
        button.title = languageNames[button.dataset.lang];
      });

      currentLanguage = nextLanguage;
      updateLanguageIndicator();
      scheduleHeroTitleFit();
      syncSeoMeta();
      window.dispatchEvent(new CustomEvent("site-language-applied"));

      if (persist) {
        localStorage.setItem("martin-nagy-language", nextLanguage);
      }
    }

    function setLanguage(language, persist = false, options = {}) {
      const nextLanguage = supportedLanguages.includes(language) ? language : "en";
      const shouldAnimate = options.animate && currentLanguage && currentLanguage !== nextLanguage && !reducedMotion.matches;

      window.clearTimeout(languageChangeTimer);

      if (!shouldAnimate) {
        document.body.classList.remove("language-changing");
        applyLanguage(nextLanguage, persist);
        return;
      }

      languageSwitcher?.classList.add("switching");
      window.setTimeout(() => languageSwitcher?.classList.remove("switching"), 460);
      document.body.classList.add("language-changing");
      languageChangeTimer = window.setTimeout(() => {
        applyLanguage(nextLanguage, persist);
        window.requestAnimationFrame(() => {
          document.body.classList.remove("language-changing");
        });
      }, 240);
    }

